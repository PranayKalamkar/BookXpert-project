CREATE DEFINER=`root`@`localhost` PROCEDURE `sp_GetMonthlyAttendanceTrends`(
    IN p_employee_id INT,
    IN p_months INT
)
BEGIN
    SELECT 
        YEAR(Date) as Year,
        MONTH(Date) as Month,
        COUNT(*) as TotalDays,
        SUM(CASE WHEN Status = 'Present' THEN 1 ELSE 0 END) as PresentDays,
        SUM(CASE WHEN Status = 'Absent' THEN 1 ELSE 0 END) as AbsentDays,
